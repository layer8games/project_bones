#include <Game/HealthPack.h>

//==========================================================================================================================
//
//Constructors	 	
//
//==========================================================================================================================
HealthPack::HealthPack(void)
	:
	_hpToRestore(1)
{  }

HealthPack::~HealthPack(void)
{  }

//==========================================================================================================================
//
//Virtual Functions
//
//==========================================================================================================================
void HealthPack::v_PickupAction(p_Soldier actor)
{
	actor->v_Heal(_hpToRestore);
	SetActive(false);
}